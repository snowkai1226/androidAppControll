package com.xk.android.entity;

public class ClockTimeInfo {
    String morningTime;
    String morningClosingTime;
    String afternoonTime;
    String afternoonClosingTime;

    public String getMorningTime() {
        return morningTime;
    }

    public void setMorningTime(String morningTime) {
        this.morningTime = morningTime;
    }

    public String getMorningClosingTime() {
        return morningClosingTime;
    }

    public void setMorningClosingTime(String morningClosingTime) {
        this.morningClosingTime = morningClosingTime;
    }

    public String getAfternoonTime() {
        return afternoonTime;
    }

    public void setAfternoonTime(String afternoonTime) {
        this.afternoonTime = afternoonTime;
    }

    public String getAfternoonClosingTime() {
        return afternoonClosingTime;
    }

    public void setAfternoonClosingTime(String afternoonClosingTime) {
        this.afternoonClosingTime = afternoonClosingTime;
    }
}
